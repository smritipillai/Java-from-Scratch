
public class Point {
	private int x;  //x-coordinate
	private int y; //y-coordinate
	
	Point(){
		this.x = 0;
		this.y = 0;
	}
	public void setX(int xCoord) {
		this.x = xCoord;
	}
	public void setY(int yCoord) {
		this.y = yCoord;
	}
	public int getX(){
		return this.x;
	}
	public int getY(){
		return this.y;
	}
}
